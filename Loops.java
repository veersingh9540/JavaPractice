public class Loops{
    public static void main(String args[]){
        // For Loop
        for (int i= 0; i<=10; i++){
            System.out.println(i);
        }
        // While Loop
        int nums = 0;
        while (nums <= 5){
            System.out.println("While loop running " + nums);
            nums++;
        }

        int nums2 = 0;
        do {
            System.out.println("This is Do-while Loop "+ nums2);
            nums2++;
        }while(nums2 < 5);

        // For git push
    }
}